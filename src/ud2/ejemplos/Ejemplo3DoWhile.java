package ud2.ejemplos;

public class Ejemplo3DoWhile {
    public static void main(String[] args) {                                                                      
        int i = 1;
        do {   //inicio del do .. while
            System.out.print(i + " ");
            i++;
        } while (i<=10);   //fin del do .. while                                                                  
        System.out.println("\nFin programa");
    }
}